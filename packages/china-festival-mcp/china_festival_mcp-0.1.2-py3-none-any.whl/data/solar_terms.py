# 节气对应的月份（大致）
SOLAR_TERM_MONTHS = {
    "小寒": 1, "大寒": 1, "立春": 2, "雨水": 2,
    "惊蛰": 3, "春分": 3, "清明": 4, "谷雨": 4,
    "立夏": 5, "小满": 5, "芒种": 6, "夏至": 6,
    "小暑": 7, "大暑": 7, "立秋": 8, "处暑": 8,
    "白露": 9, "秋分": 9, "寒露": 10, "霜降": 10,
    "立冬": 11, "小雪": 11, "大雪": 12, "冬至": 12
}

def _decode_solar_term_data(year: int, term_index: int) -> int:
    """
    从SOLAR_TERMS_DATA中解码指定年份和节气的日期
    
    Args:
        year: 年份 (1901-2049)
        term_index: 节气索引 (0-23)
        
    Returns:
        节气日期
    """
    if year < 1901 or year > 2049:
        raise ValueError(f"年份 {year} 超出支持范围 (1901-2049)")
    
    if term_index < 0 or term_index > 23:
        raise ValueError(f"节气索引 {term_index} 超出范围 (0-23)")
    
    # 计算在SOLAR_TERMS_DATA中的位置
    # 每年有12个字节，每个月一个字节，包含两个节气的日期信息
    year_offset = year - 1901
    month_index = term_index // 2  # 每月两个节气
    byte_index = year_offset * 12 + month_index
    
    # 检查数据索引是否超出范围
    if byte_index >= len(SOLAR_TERMS_DATA):
        raise ValueError(f"数据索引 {byte_index} 超出范围")
    
    # 获取字节数据
    byte_data = SOLAR_TERMS_DATA[byte_index]
    
    # 解码节气日期
    # 根据CalendarConvert.py的算法：
    # 高4位存储第一个节气的偏移（15减去该值）
    # 低4位存储第二个节气的偏移（该值加15）
    if term_index % 2 == 0:  # 偶数索引，第一个节气，使用高4位
        offset = (byte_data >> 4) & 0x0F
        day = 15 - offset
    else:  # 奇数索引，第二个节气，使用低4位
        offset = byte_data & 0x0F
        day = offset + 15
    
    return day


def get_solar_term_for_date(year: int, month: int, day: int) -> Optional[str]:
    """获取指定日期的节气"""
    # 检查年份范围
    if year < 1901 or year > 2049:
        return None
    
    # 遍历所有节气，查找匹配的日期
    for i, term in enumerate(SOLAR_TERMS):
        try:
            term_day = _decode_solar_term_data(year, i)
            term_month = SOLAR_TERM_MONTHS[term]
            
            if month == term_month and day == term_day:
                return term
        except ValueError:
            continue
    
    return None

def get_solar_terms_for_month(year: int, month: int) -> List[Tuple[str, int]]:
    """获取指定月份的所有节气"""
    if year < 1901 or year > 2049:
        return []
    
    terms = []
    
    for i, term in enumerate(SOLAR_TERMS):
        try:
            term_month = SOLAR_TERM_MONTHS[term]
            if term_month == month:
                term_day = _decode_solar_term_data(year, i)
                terms.append((term, term_day))
        except ValueError:
            continue
    
    return sorted(terms, key=lambda x: x[1])

def get_next_solar_term(year: int, month: int, day: int) -> Optional[Tuple[str, datetime]]:
    """获取下一个节气"""
    if year < 1901 or year > 2049:
        return None
    
    current_date = datetime(year, month, day)
    
    # 查找当年剩余的节气
    next_terms = []
    
    for i, term in enumerate(SOLAR_TERMS):
        try:
            term_month = SOLAR_TERM_MONTHS[term]
            term_day = _decode_solar_term_data(year, i)
            term_date = datetime(year, term_month, term_day)
            
            if term_date > current_date:
                next_terms.append((term, term_date))
        except ValueError:
            continue
    
    if next_terms:
        next_terms.sort(key=lambda x: x[1])
        return next_terms[0]
    
    # 如果当年没有剩余节气，返回下一年的第一个节气
    if year + 1 <= 2049:
        try:
            next_year_term_day = _decode_solar_term_data(year + 1, 0)  # 小寒
            next_year_first = datetime(year + 1, 1, next_year_term_day)
            return ("小寒", next_year_first)
        except ValueError:
            return None
    
    return None

def get_season_by_solar_term(term: str) -> str:
    """根据节气获取季节"""
    spring_terms = ["立春", "雨水", "惊蛰", "春分", "清明", "谷雨"]
    summer_terms = ["立夏", "小满", "芒种", "夏至", "小暑", "大暑"]
    autumn_terms = ["立秋", "处暑", "白露", "秋分", "寒露", "霜降"]
    winter_terms = ["立冬", "小雪", "大雪", "冬至", "小寒", "大寒"]
    
    if term in spring_terms:
        return "春"
    elif term in summer_terms:
        return "夏"
    elif term in autumn_terms:
        return "秋"
    elif term in winter_terms:
        return "冬"
    else:
        return "未知"


def get_solar_term_date(year: int, term: str) -> Optional[datetime]:
    """
    获取指定年份和节气的精确日期
    
    Args:
        year: 年份 (1901-2049)
        term: 节气名称
        
    Returns:
        节气日期，如果年份超出范围或节气不存在则返回None
    """
    if year < 1901 or year > 2049:
        return None
    
    if term not in SOLAR_TERMS:
        return None
    
    try:
        term_index = SOLAR_TERMS.index(term)
        term_day = _decode_solar_term_data(year, term_index)
        term_month = SOLAR_TERM_MONTHS[term]
        
        return datetime(year, term_month, term_day)
    except (ValueError, IndexError):
        return None


def get_all_solar_terms_for_year(year: int) -> List[Tuple[str, datetime]]:
    """
    获取指定年份的所有节气日期
    
    Args:
        year: 年份 (1901-2049)
        
    Returns:
        包含所有节气名称和日期的列表，按时间顺序排序
    """
    if year < 1901 or year > 2049:
        return []
    
    terms = []
    
    for i, term in enumerate(SOLAR_TERMS):
        try:
            term_day = _decode_solar_term_data(year, i)
            term_month = SOLAR_TERM_MONTHS[term]
            term_date = datetime(year, term_month, term_day)
            terms.append((term, term_date))
        except ValueError:
            continue
    
    # 按日期排序
    return sorted(terms, key=lambda x: x[1])